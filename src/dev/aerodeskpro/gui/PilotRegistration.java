    public void loadPilotsToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        try {
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/aerodeskpro", "root", "1234");

            String sql = "SELECT pilot_id, first_name, last_name, nic, dob, mobile, email FROM pilot";
            PreparedStatement ps = c.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("pilot_id");
                String fname = rs.getString("first_name");
                String lname = rs.getString("last_name");
                String nic = rs.getString("nic");
                String dob = rs.getString("dob");
                String mobile = rs.getString("mobile");
                String email = rs.getString("email");

                model.addRow(new Object[]{id, fname, lname, nic, dob, mobile, email});
            }

            rs.close();
            ps.close();
            c.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading pilots: " + e.getMessage());
        }
    }

