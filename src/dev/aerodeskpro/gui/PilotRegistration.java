    private void roundedButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundedButton2ActionPerformed
        // TODO add your handling code here:
        String fname = jTextField1.getText().trim();
        String lname = jTextField2.getText().trim();
        String email = jTextField3.getText().trim();
        String mobile = jTextField5.getText().trim();
        String nic = jTextField6.getText().trim();

        // Assuming customDatePicker1.getDate() returns java.util.Date
        Date selectedDate = customDatePicker1.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Please select a date of birth.");
            return;
        }

        String dob = new SimpleDateFormat("yyyy-MM-dd").format(selectedDate);

        if (fname.isEmpty() || lname.isEmpty() || email.isEmpty() || mobile.isEmpty() || nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        try {
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/aerodeskpro", "root", "1234");

            String sql = "INSERT INTO pilot (first_name, last_name, nic, dob, mobile, email) "
                    + "VALUES (?, ?, ?, ?, ?, ?)";

            PreparedStatement ps = c.prepareStatement(sql);
            ps.setString(1, fname);
            ps.setString(2, lname);
            ps.setString(3, nic);
            ps.setString(4, dob);
            ps.setString(5, mobile);
            ps.setString(6, email);

            int result = ps.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Pilot added successfully.");
                loadPilotsToTable();  // You should define this method to refresh JTable
                clearInputFields();   // Clear form fields after insertion
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add pilot.");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_roundedButton2ActionPerformed

